    public void bañarMascota() {
        timer.start();
        this.lblPerrito.setIcon(new ImageIcon("src/Elementos_Graficos/perrito bañandose.gif"));
    }

    public void detenerBaño() {
        timer.stop();
        juego.incrementarExperiencia(6);
        this.lblPerrito.setIcon(new ImageIcon("src/Elementos_Graficos/perritoPastorAleman.gif"));
        estaBañando = false;
    }

