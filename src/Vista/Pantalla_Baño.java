    private void btnBañarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBañarActionPerformed
        if (!estaBañando) {
            if (this.juego.getMascota().getHigiene() <= 90) {
                this.bañarMascota();
                this.estaBañando = true;
            } else {
                PanelMensaje pm = new PanelMensaje((JFrame) SwingUtilities.getWindowAncestor(this), true, "<html>TU MASCOTA ESTA LIMPIA <br> NO NECESITAS BAÑARLO</html>", 3);
                pm.setVisible(true);
                //JOptionPane.showMessageDialog(this, "Tu mascota esta limpia no necesitas bañarlo");
            }
        }
    }//GEN-LAST:event_btnBañarActionPerformed

