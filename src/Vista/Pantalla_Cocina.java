    private boolean comer(int cantComida) {
        if (juego.getMascota().getHambre() >= 99) {
            PanelMensaje pm = new PanelMensaje((JFrame) SwingUtilities.getWindowAncestor(this), true, "<html>TU MASCOTA ESTA <br> SATISFECHA</html>", 3);
            pm.setVisible(true);
            //JOptionPane.showMessageDialog(this, "Tu mascota ya esta satisfecha");
            return false;
        } else {
            int valor = juego.getMascota().getHambre() + cantComida;
            juego.getPbHambre().setValue(valor);
            juego.getMascota().setHambre(valor);
            this.lblPerrito.setIcon(new ImageIcon("src/Elementos_Graficos/perrito comiendo.gif"));
            return true;
        }
    }

    private boolean curar(int cantCura) {
        if (juego.getMascota().getVida() >= 99) {
            PanelMensaje pm = new PanelMensaje((JFrame) SwingUtilities.getWindowAncestor(this), true, "<html>TU MASCOTA TIENE <br> SU SALUD AL MAXIMO</html>", 3);
            pm.setVisible(true);
            //JOptionPane.showMessageDialog(this, "Tu mascota tiene su salud al maximo");
            return false;
        } else {
            int valor = juego.getMascota().getVida() + cantCura;
            juego.getPbVida().setValue(valor);
            juego.getMascota().setVida(valor);
            this.lblPerrito.setIcon(new ImageIcon("src/Elementos_Graficos/perrito comiendo.gif"));
            return true;
        }
    }
