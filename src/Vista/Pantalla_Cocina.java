    private void btnPizzaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPizzaActionPerformed
        int cantPizzas = Integer.valueOf(this.lblcantPizza.getText());
        if (cantPizzas == 0) {
            PanelMensaje pm = new PanelMensaje((JFrame) SwingUtilities.getWindowAncestor(this), true, "<html>NO TIENES ESTE PRODUCTO<br> COMPRALO EN LA TIENDA</html>", 3);
            pm.setVisible(true);
            //JOptionPane.showMessageDialog(this, "No tienes este producto compralo en la tienda");
        } else {
            boolean siComio = this.comer(5);
            if (siComio) {
                this.juego.getMascota().setCantPizzas(juego.getMascota().getCantPizzas() - 1);
                this.lblcantPizza.setText(String.valueOf(cantPizzas - 1));
                this.juego.incrementarExperiencia(4);
            }
        }
    }//GEN-LAST:event_btnPizzaActionPerformed

